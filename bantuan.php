		<h3 align="center"><font align="center">Halaman Bantuan</font></h3>
  <br><h3 align="center">Paud Nur Rahma Tanjung</h3>
<br>
				<p><strong>  Paud adalah pendidikan usua dini yang dapat membuat anak2 semakin bisa berkreasi dengan kemampuan yang dimilikinya Setiap kali  memasuki  tahun ajaran   baru,  orang sibuk  mengurusi  anak-anaknya  mau sekolah   dimana.  Agaar anaknya kelak   menjadi matang  ke  sekolah  yang  lebih  lanjut.  Berbagai  masukan  berdatangan  mulai  dari   informasi   teman  atau  saudara-saudaranya.  Kadang-kadang orang tua  bingung untuk menentukan  pilihan  .  Mau  sekolah atau tidak  , kalau  sekolaah perlu  biaya.  Itulah  masalahnya  yang dihadapi  oleh  masyarakat  Sidorejo sebagian besar . Mau sekolah,  sekolahnya jauh  diluar desa ,pada hal  anaaknya tidak  mau  ditinggal . Banyak  permasalahan yang dihadapi.  Karena mereka yang bercukupan  tidak ada masalah , karena mereka sangat menyadari pentingnya  pendidikan anak usia dini dalam menempa  karakter dan bekal anak kelak ketika akan memasuki sekolah dasar atau  sekolah yang  lebih  lanjut.
Dikarenakan sebagian besar  masyarakat Sidorejo adalah kalangan  orang petani orangnya  masih  sederhana.  Masalahnya sekarang adalah  bagaimana dengan keluarga yang tidak mampu dengan keserhanaanya?  Pastilah mereka sangat  bingung untuk memikirkan anak-anaknya bersekolah . Dengan keberadaan masyarakat di Sidorejo seperti itulah  Ibu  Kepala Desa merasa tergugah hatinya, bagaimana dengan masyarakatnya  sekarang.  Pada hal didesa sekitarnya sudah banyak sekolah-sekolah,diluar sekolah dasar sebelum sekolah dasar. Pada hal sekolah dasar harus pandai dan mampu, sedangkan anak-anak pada usia  sebelum memasuki sekolah dasar banyak lagi.  Karena itulah agar  anak  dimasasekolah dasar  lebih  matang dan  pandai,  maka perlu adanya sekolah, wadah untuk menghadapi yaitu pra sekolah.  Dan bias disebut sekolah atau pendidikan  non  formal.  Sebelum menghadapi tahun ajaran baru,  Ibu Kepala Desa  mengajukan proposal dengan dinas terkait.  Dengan maksud dan tujuan adalah ingin menampung anak-anak  pada  usia sebelum nya. maka untuk usia 3-4th dan 4-5th diberi wadah atau layanan di PAUD  CEMERLANG  yang akan didirikan.  Dinamakan CEMERLANG dikandung maksud karena biar  anak yang masukdi PAUD  melangkah sekolah yang lebih lanjut.  Setelah mengajukan proposal kemudian dicari  guru atau pengasuh anak-anak. Selang satu setengah bulan, Ibu Kepala Desa memutuskan tempat belajarnya. Ditentukan pula tempat karena belum ada  tempat yang khusus ataudibuatkan gedung , makauntuk tempat sementara di rumah Kepala Desa sebelum di buatkan gedung. Disediakan pula APE   sebagai media belajar.
 Saat ajaran baru  tiba dibuka pendaftaran anak PAUD. Sebelum dibuka pendaftaran, setiap posyandu diberi  informasi , masukan-masukan di beri pengarahan pada orang tua yang punya anak 3-4 th dan 4-5 th untuk  menyadari pentingnya pendidikan sebelum sekolah dasar. Alhamdulilah, kesadaran masyarakat para orang  tua  akan pentingnya pendidikan pra  sekolah mereka mendaftarkan anaknya di PAUD CEMERLANG. Untuk tahun ajaran pertama 17 anak telah masuk  di PAUD. Cukup  menggembirakan hampir  semua umur  masuk.  Dengan jumlah pengasuh dua orang.  Dengan peralatan sederhana, tempat yang sederhana, ternyata anak-anak sangat gembira dan   desa lain .
Dengan  perjalanan waktu tidak terasa sudah dua semester .  Di lihat  dari anak-anak mereka cukup pandai, dan cepat menerima apa yang disampaikan  guru atau pengasuhnya.  Sebenarnya anak seumur 3-4 th dan  4-5 th  dan perlu adanya  rangsangan-rangsangan pendidikan untuk membantu pertumbuhan, perkembabgan jasmani dan  rohani agar anak memiliki  kesiapan  dalam  memasuki jenjang pendidikan  yang  lebih  lanjut .  Banyak  upaya-upaya yang dilakukan agar PAUD CEMERLANG  melangkah lebih baik.  Diantaranya  yaitu studi banding  di PAUD  di desa  yang telah ada lebih dahulu, guru atau pengasuh  selalu mengikuti  kegiatan di HIMPAUDI   dan membaca buku tentang PAUD  yang lebih modern, APE  ditambah sesuai kebutuhan.  Dari orang tua pun sangat diperlukan yaitu keaktifan  siswa untuk masuk sekolah apabila orang tua sibuk bekerja di rumah.
Harapan menjadi PAUD yang terbaik itulah cita-cita yang harus dicanangkan.  Terbaik bagi anak-anak, desanya  dan para orang tua.  Menjadi  PAUD yang bermanfaat bagi anak untuk melanjutkan jenjang sekolah  berikutnya, PAUD yang kuantitatif  maupun kwalitatif dalam pendidikan usia dini. Dan dukungan dari semua pihak  baik pemerintah, masyarakat atau pun dinas terkait dalam PAUD.     </strong></p>
							
